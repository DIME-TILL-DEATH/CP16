inline float  __attribute__ ((always_inline)) read_mem(uint32_t ind_mem)
{
	float acc;
	//uint32_t i = (early_point+ind_mem)&0x7fff;
	//if(i <= 0x3fff)acc = early_buf[i];
	//else acc = early_buf1[i & 0x3fff];
	acc = early_buf[(early_point+ind_mem)&0x1fff];
	return acc;
}
inline void  __attribute__ ((always_inline)) RZP (uint32_t ind_mem,float koef_mem)
{
  accum = read_mem(ind_mem)*koef_mem;
}
inline void __attribute__ ((always_inline)) RAP (uint32_t ind_mem,float koef_mem)
{
  accum += read_mem(ind_mem)*koef_mem;
}
inline void __attribute__ ((always_inline)) WAP (uint32_t ind_mem,float koef_mem)
{
  stor_mem(ind_mem);
  accum += accum*koef_mem;
}
inline void __attribute__ ((always_inline)) WZP (uint32_t ind_mem,float koef_mem)
{
  stor_mem(ind_mem);
  accum = accum*koef_mem;
}
inline void __attribute__ ((always_inline)) WAPC (uint32_t ind_mem,float koef_mem)
{
  stor_mem(ind_mem);
  accum += accum*koef_mem;
  C = accum;
}
inline void __attribute__ ((always_inline)) WCP (uint32_t ind_mem,float koef_mem)
{
  stor_mem(ind_mem);
  accum = C + accum*koef_mem;
}
inline void __attribute__ ((always_inline)) chor(uint32_t adr)
{
	  phase2 += 0.0001f ;
	  if ( phase2 >= 255.0f ) phase2 = 0.0f;
	  if (phase2 < 128.0f)phase3 = phase2;
	  else phase3 = 255.0f - phase2;
	  uint32_t  poschor = phase3;
	  frac = phase3 - poschor;
	  poschor = (poschor + adr)&0x7fff;
	  accum = (1.0f - frac )*read_mem(poschor) + frac * read_mem(poschor + 1.0f);
}
